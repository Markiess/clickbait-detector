# 8: Training Loop
# ============================================================
def train_student(
    model,
    train_loader,
    val_loader,
    epochs=3,
    lr=2e-5,
    temperature=2.0,
    alpha=0.25,
    beta=0
):
    """Train student model with knowledge distillation."""
    optimizer = torch.optim.AdamW(model.parameters(), lr=lr)
    best_f1 = 0.0

    for epoch in range(epochs):
        model.train()
        total_loss = 0.0

        for batch in tqdm(train_loader, desc=f"Epoch {epoch+1}/{epochs}"):
            optimizer.zero_grad()

            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            chatgpt_soft = batch['chatgpt_soft'].to(device)
            human_soft = batch['human_soft'].to(device)
            hard_labels = batch['hard_label'].to(device)

            logits = model(input_ids, attention_mask)
            loss = distillation_loss(
                logits,
                chatgpt_soft,
                human_soft,
                hard_labels=hard_labels,
                temperature=temperature,
                alpha=alpha,
                beta=beta
            )

            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        # Validation
        val_f1, val_acc = evaluate_model(model, val_loader)

        avg_loss = total_loss / len(train_loader)
        print(f"Epoch {epoch+1} | Train Loss: {avg_loss:.4f} | "
              f"Val Acc: {val_acc:.4f} | Val F1: {val_f1:.4f}")

        if val_f1 > best_f1:
            best_f1 = val_f1
            torch.save(model.state_dict(), 'distilled_student_best.pt')

    return best_f1

def evaluate_model(model, data_loader):
    """Evaluate model and return F1 and accuracy scores."""
    model.eval()
    all_preds, all_labels = [], []

